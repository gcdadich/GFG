class Solution {
    int countPairs(int mat1[][], int mat2[][], int n, int x) {
        // code here
        int count = 0,i = 0,j = 0,a=n-1,b=n-1;
        while(i<n && j<n && a>=0 && b>=0){
            int sum = mat1[i][j]+mat2[a][b];
            if(sum==x){
                count++;
                j++;
                b--;
            }else if(sum<x){
                j++;
            }else
               b--;
               if(b<0){
                   b=n-1;
                   a--;
               }
               if(j>=n){
                   j=0;
                   i++;
               }
        }
        return count;
    }
}